  <script>
    const get = selector => document.querySelector(selector);

    const m_menu_trigger = get(".m-menu-trigger");
    const mobile_nav = get(".mobile-nav");
    const overlay_shadow = get(".overlay-shadow");

    m_menu_trigger.addEventListener("click", function(e){
        mobile_nav.classList.add("slide-in-show");

        overlay_shadow.classList.add("slide-in-show");
    });

    overlay_shadow.addEventListener("click", function(e){
        overlay_shadow.classList.toggle("slide-in-show");
        mobile_nav.classList.toggle("slide-in-show");
    });


    const setClickFocus = (clickTarget, focusTarget) => {
        if(!clickTarget || !focusTarget) return false;

        clickTarget.addEventListener("click", function(e){
            e.preventDefault();
            focusTarget.focus();
        });
    }

    // d-v
    setClickFocus(get(".d-view .em_field_desc"), get(".d-view #eml_field"));

    setClickFocus(get(".d-view .pwd_field_desc"), get(".d-view #pwd_field"));

    setClickFocus(get(".d-view .inner-container .btn-container span"), get(".d-view .inner-container input[type='text']"));


    // m-v
    setClickFocus(get(".m-view .em_field_desc"), get(".m-view #eml_field"));

    setClickFocus(get(".m-view .pwd_field_desc"), get(".m-view #pwd_field"));

    setClickFocus(get(".m-view .inner-container .btn-container span"), get(".m-view .inner-container input[type='text']"));


    // hang label on val

    
    const onBlurHang = elem => elem.addEventListener("blur", function(e){
        e.preventDefault();

        if (!elem?.value && elem.value !== "") return false;

        if(elem.value.length > 0){
            elem.classList.remove("has-value");
            elem.classList.add("has-value");
        }else{
            elem.classList.remove("has-value");
        }
    });

    onBlurHang(get(".m-view .em_field_label input"));
    onBlurHang(get(".m-view .pwd_field_container input#pwd_field"));

    onBlurHang(get(".d-view .em_field_label input"));
    onBlurHang(get(".d-view .pwd_field_container input#pwd_field"));

    onBlurHang(get(".d-view .bottom-menu-right-input-container .inner-container input"));

